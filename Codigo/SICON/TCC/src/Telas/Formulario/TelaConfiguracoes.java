private void jbtRestaurarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtRestaurarActionPerformed
        try {
            Controller.RestaurarConfiguracoes();//GEN-LAST:event_jbtRestaurarActionPerformed
            Controller.ComporConfiguracoesForm(this);
            SetarCampos();
            mensagem.ExibirMensagem(18);
        } catch (SQLException ex) {
            Logger.getLogger(TelaConfiguracoes.class.getName()).log(Level.SEVERE, null, ex);
            mensagem.ExibirMensagem(3);
        } catch (Exception ex) {
            Logger.getLogger(TelaConfiguracoes.class.getName()).log(Level.SEVERE, null, ex);
            mensagem.ExibirMensagem(3);
        }
}

private void jbtSalvarMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jbtSalvarMousePressed
        try {    
            CarregarCampos();
            Controller.SalvarAlteracoes(this);
            mensagem.ExibirMensagem(2);
        } catch (SQLException ex) {
            mensagem.ExibirMensagem(3);
        } catch (Exception ex) {
            mensagem.ExibirMensagem(3);
        }// TODO add your handling code here:
}//GEN-LAST:event_jbtSalvarMousePressed

private void CarregarCampos(){
    remetente = jtfRemetente.getText();
    senha = Funcoes.CharToString(jpfSenha.getPassword());
    TituloMensagem = jtfTituloMensagem.getText();
    TextoMensagem = jtaTextoMensagem.getText();
}
